--Project 1 DB
CREATE TABLE EMPLOYEE
(
    EMP_ID NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 UNIQUE,
    FNAME VARCHAR2(20),
    LNAME VARCHAR2(20),
    EMAIL VARCHAR2(50) CONSTRAINT PK_EMAIL PRIMARY KEY,
    PASSWORD VARCHAR2(50),
    REPORTSTO NUMBER(5) DEFAULT NULL,
    LOGGEDIN NUMBER(5) DEFAULT 0
)

CREATE TABLE REIMBURSEMENTS
(
    R_ID NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY INCREMENT BY 1 UNIQUE,
    EMP_ID Number(5),
    CONTENT VARCHAR2(2000),
    AmountRequested Number(6,2),
    Reimbursement_Picture BLOB,
    Resolved Number(1) DEFAULT 0,
    ResolvedMessage Varchar2(200) DEFAULT 'Pending',
    MngResolved Number(5) Default 0
)

