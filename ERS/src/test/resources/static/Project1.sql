CREATE TABLE EMPLOYEE (
    EMP_ID NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    EMAIL VARCHAR2(25) CONSTRAINT PK_EMPLOYEE PRIMARY KEY,
    PASS VARCHAR2(25) NOT NULL,
    FIRST_NAME VARCHAR2(20) NOT NULL,
    LAST_NAME VARCHAR2(20) NOT NULL,
    REPORTSTO NUMBER(20)
);

CREATE TABLE EXPENSE (
    EXP_ID NUMBER(5) GENERATED BY DEFAULT ON NULL AS IDENTITY START WITH 1 INCREMENT BY 1,
    EMAIL VARCHAR2(25) ,
    PRICE NUMBER(5,2) NOT NULL,
    STATUS VARCHAR2(20) NOT NULL,
    RESOLVEDBY VARCHAR2(20)
);

